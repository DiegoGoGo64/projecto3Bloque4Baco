package pooconmenu;

public class profesor {

//Atributos
    private String nombre, materia;

    public profesor(String nombre, String materia) {

        this.nombre = nombre;

        this.materia = materia;

    }

    public String verNombre() {

        return this.nombre;

    }

    public String verMateria() {

        return this.materia;

    }

}
